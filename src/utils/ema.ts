export const ALPHA = 0.3;

export function calcEMA(prevEMA: number, newMinutes: number): number {
  return ALPHA * newMinutes + (1 - ALPHA) * prevEMA;
}

export function calcMatchScore(emaMinutes: number, actualMinutes: number): number {
  const diff = Math.abs(emaMinutes - actualMinutes);
  return Math.max(0, 1 - diff / 60);
}

export function timeToMinutes(time: string): number {
  const [h, m] = time.split(':').map(Number);
  return h * 60 + m;
}

export function minutesToTime(minutes: number): string {
  const h = Math.floor(minutes / 60) % 24;
  const m = Math.round(minutes % 60);
  return `${String(h).padStart(2, '0')}:${String(m).padStart(2, '0')}`;
}

export function formatDate(date: Date): string {
  const y = date.getFullYear();
  const m = String(date.getMonth() + 1).padStart(2, '0');
  const d = String(date.getDate()).padStart(2, '0');
  return `${y}-${m}-${d}`;
}

export function formatTime(date: Date): string {
  const h = String(date.getHours()).padStart(2, '0');
  const m = String(date.getMinutes()).padStart(2, '0');
  return `${h}:${m}`;
}
